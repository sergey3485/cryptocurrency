import * as React from 'react';
import Head from 'next/head';
import Link from 'next/link';

import { CoreLayout } from '@/layouts/core/CoreLayout';

import { Coin } from '../modules/cryptocurrencies/api';

const Home = () => {
  const [allCoins, setAllCoins] = React.useState<Coin[]>();

  React.useEffect(() => {
    fetch('/api/cryptocurrencies/coins')
      .then((data) => data.json())
      .then((data) => setAllCoins(data as Coin[]))
      .catch((error) => console.log(error));
  }, []);

  return (
    <CoreLayout>
      <Head>
        <title>Cryptocurrencies</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {allCoins?.map((data) => (
        <Link href={`/cryptocurrencies/coins/${data.uuid}`} passHref key={data.uuid}>
          <a>{data.name}</a>
        </Link>
      ))}
    </CoreLayout>
  );
};

export default Home;
